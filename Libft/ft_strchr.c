#include "libft.h"

char	*ft_strchr(const char *s, int n)
{
	int		i;
	char	c;
	char	*str;

	i = 0;
	c = (char)n;
	str = (char *)s;
	while (str[i] != '\0')
	{
		if (str[i] == c)
			return (&str[i]);
		i++;
	}
	if (c == '\0')
		return (&str[i]);
	return (0);
}
